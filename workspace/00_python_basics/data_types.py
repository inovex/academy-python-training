# Integer
my_int = 42
my_int = int(42.0)

print(my_int)
print(type(my_int))

# Float
my_float = 42.
my_float = float(42)

print(my_float)
print(type(my_float))

# Boolean
my_bool = True
my_bool = bool("True")

print(my_bool)
print(type(my_bool))

# String
my_str = "Hermione Granger"
my_str = str(42)

print(my_str)
print(type(my_str))